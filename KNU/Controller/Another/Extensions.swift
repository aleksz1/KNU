extension Substring {
    
    public func makeBold() -> NSAttributedString {
        let attributes: [NSAttributedStringKey: Any] = [.font: UIFont(name: "AvenirNext-Medium", size: 13)!]
        let boldString = NSMutableAttributedString(string: String(self), attributes: attributes)
        return boldString
    }
    
    public func makeItalic() -> NSAttributedString {
        let attributes: [NSAttributedStringKey: Any] = [.font: UIFont(name: "Avenir-BookOblique", size: 13)!]
        let boldString = NSMutableAttributedString(string: String(self), attributes: attributes)
        return boldString
    }
}
extension UITextView {
    
    public func centerText() {
        self.textAlignment = .center
        let fittingSize = CGSize(width: bounds.width, height: CGFloat.greatestFiniteMagnitude)
        let size = sizeThatFits(fittingSize)
        let topOffset = (bounds.size.height - size.height * zoomScale) / 2
        let positiveTopOffset = max(1, topOffset)
        contentOffset.y = -positiveTopOffset
    }
}
extension UIView {
    
    func addVerticalGradientLayer(topColor:UIColor, bottomColor:UIColor) {
        let gradient = CAGradientLayer()
        gradient.frame = self.bounds
        gradient.colors = [
            topColor.cgColor,
            bottomColor.cgColor
        ]
        gradient.locations = [0.0, 1.0]
        gradient.startPoint = CGPoint(x: 0, y: 0)
        gradient.endPoint = CGPoint(x: 0, y: 1)
        self.layer.insertSublayer(gradient, at: 0)
    }
}

extension UIViewController {
    func hideKeyboardWhenTappedAround() {
        let tap: UITapGestureRecognizer = UITapGestureRecognizer(target: self, action: #selector(UIViewController.dismissKeyboard))
        tap.cancelsTouchesInView = false
        view.addGestureRecognizer(tap)
    }
    @objc func dismissKeyboard() {
        view.endEditing(true)
    }
}
